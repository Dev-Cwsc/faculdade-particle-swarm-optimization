package br.com.dev.cwsc.particleswarmoptimization;

public class Coordenada
{
    private double x1;
    private double x2;

    public void setCoord(double x1, double x2)
    {
        this.x1 = x1;
        this.x2 = x2;
    }

    public double get_x1()
    {
        return this.x1;
    }

    public double get_x2()
    {
        return this.x2;
    }
}
